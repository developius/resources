def encrypt(text):
	text = text.lower()
	toReturn = []
	for c in text:
		try: toReturn.append(codes[c])
		except: pass
	return(" ".join(toReturn))

def decrypt(text):
	toReturn = []
	text = text.split(" ")
	for code in text:
		for key, val in codes.iteritems():
			if val == code:
				toReturn.append(key)
	return("".join(toReturn).replace("/"," "))
